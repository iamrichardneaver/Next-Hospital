    <div class="card-body small">
        <p class="mb-2"><strong>Manual expense amount</strong> = the amount you enter. There is no automatic calculation for operational expenses.</p>
        <p class="mb-2"><strong>Inventory purchases</strong> (pharmacy/lab stock) are recorded automatically when goods are received on a purchase order — do not duplicate those here.</p>
        <p class="mb-2"><strong>Pending</strong> expenses are <em>not</em> included in accounting KPIs or reports until an accountant approves them.</p>
        <p class="mb-2"><strong>Approved expenses</strong> are summed in Accounting Hub totals:</p>
        <ul class="mb-2">
            <li>Total Expenses = SUM(approved + paid manual expenses) + auto inventory expenses</li>
            <li>Net Income = Revenue − Approved Expenses</li>
        </ul>
        <p class="mb-0 text-muted">After approval, an accountant may mark the expense as <em>paid</em> when reimbursement is complete.</p>
